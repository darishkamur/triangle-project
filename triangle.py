#Функция вычисляет площадь треугольника
#Функция принимает длину высоты и стороны, на которую опирается высота, возвращает площадь
#
## Пример:
# >>> area(4, 5)
# 10
def area(a, h):
    return a * h / 2
#Функция вычисляет периметр треугольника
#Функция принимает длину всех сторон треугольника, возвращает периметр
#
## Пример:
# >>> perimeter(4, 5, 6)
# 15
def perimeter(a, b, c):
    return a + b + c
